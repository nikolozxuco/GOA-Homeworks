import React, { useState } from 'react';

export default function ConditionalRendering() {
  const [isLoggedIn, setIsLoggedIn] = useState(false);
  const [userRole, setUserRole] = useState('user');
  const [count, setCount] = useState(0);


  // if statement рЃўрЃА рЃњрЃљрЃЏрЃЮрЃДрЃћрЃюрЃћрЃЉрЃљ JSX-рЃўрЃА рЃњрЃљрЃарЃћрЃЌ, рЃърЃўрЃарЃЊрЃљрЃърЃўрЃарЃў render-рЃерЃў рЃљрЃа рЃерЃћрЃўрЃФрЃџрЃћрЃЉрЃљ
  // рЃўрЃА рЃерЃћрЃўрЃФрЃџрЃћрЃЉрЃљ рЃњрЃљрЃЏрЃЮрЃЋрЃўрЃДрЃћрЃюрЃЮрЃЌ рЃцрЃБрЃюрЃЦрЃфрЃўрЃўрЃА рЃерЃўрЃњрЃюрЃўрЃЌ, рЃАрЃљрЃюрЃљрЃЏ return рЃЊрЃљрЃАрЃгрЃћрЃарЃЌ
  
  let content;
  if (isLoggedIn) {
    content = <h1>рЃЏрЃЮрЃњрЃћрЃАрЃљрЃџрЃЏрЃћрЃЉрЃўрЃЌ, рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃЮ!</h1>;
  } else {
    content = <h1>рЃњрЃЌрЃ«рЃЮрЃЋрЃЌ, рЃерЃћрЃАрЃЋрЃўрЃЊрЃћрЃЌ рЃљрЃюрЃњрЃљрЃарЃўрЃерЃќрЃћ</h1>;
  }

  // if statement рЃерЃћрЃўрЃФрЃџрЃћрЃЉрЃљ рЃњрЃљрЃЏрЃЮрЃЋрЃўрЃДрЃћрЃюрЃЮрЃЌ рЃърЃўрЃарЃЊрЃљрЃърЃўрЃа рЃцрЃБрЃюрЃЦрЃфрЃўрЃљрЃерЃў
  function renderMessage() {
    if (count > 5) {
      return <p>рЃарЃўрЃфрЃ«рЃЋрЃў рЃЊрЃўрЃЊрЃўрЃљ!</p>;
    } else if (count > 0) {
      return <p>рЃарЃўрЃфрЃ«рЃЋрЃў рЃърЃљрЃбрЃљрЃарЃљрЃљ</p>;
    } else {
      return <p>рЃарЃўрЃфрЃ«рЃЋрЃў рЃюрЃБрЃџрЃЮрЃЋрЃљрЃюрЃўрЃљ</p>;
    }
  }

 

  // Ternary operator-рЃў рЃЏрЃБрЃерЃљрЃЮрЃЉрЃА JSX-рЃўрЃА рЃерЃўрЃњрЃюрЃўрЃЌ (curly braces-рЃўрЃА рЃЊрЃљрЃБрЃфрЃЋрЃћрЃџ)
  // рЃАрЃўрЃюрЃбрЃљрЃЦрЃАрЃў: condition ? valueIfTrue : valueIfFalse
  // рЃћрЃА рЃЉрЃћрЃЋрЃарЃљрЃЊ рЃБрЃцрЃарЃЮ рЃЏрЃЮрЃЎрЃџрЃћ рЃЊрЃљ рЃЏрЃЮрЃАрЃљрЃ«рЃћрЃарЃ«рЃћрЃЉрЃћрЃџрЃўрЃљ react-рЃерЃў

  return (
    <div>
    
      {content}

 
      <h2>
        {isLoggedIn ? 'рЃЌрЃЦрЃЋрЃћрЃю рЃЊрЃљрЃцрЃўрЃЦрЃАрЃўрЃарЃћрЃЉрЃБрЃџрЃў рЃ«рЃљрЃарЃЌ' : 'рЃљрЃа рЃ«рЃљрЃарЃЌ рЃЊрЃљрЃцрЃўрЃЦрЃАрЃўрЃарЃћрЃЉрЃБрЃџрЃў'}
      </h2>


      <p>
        {count > 10 ? 'РюЊрЃЊрЃўрЃЊрЃў рЃарЃўрЃфрЃ«рЃЋрЃў' : ' рЃърЃљрЃбрЃљрЃарЃљ рЃарЃўрЃфрЃ«рЃЋрЃў'}
      </p>

  
      <p>
        {userRole === 'admin' ? ' рЃљрЃЊрЃЏрЃўрЃюрЃў' : userRole === 'moderator' ? ' рЃЏрЃЮрЃЊрЃћрЃарЃљрЃбрЃЮрЃарЃў' : ' рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў'}
      </p>

      {/* && operator рЃЏрЃБрЃерЃљрЃЮрЃЉрЃА рЃљрЃАрЃћ:
          - рЃЌрЃБ рЃърЃўрЃарЃЋрЃћрЃџрЃў рЃљрЃарЃњрЃБрЃЏрЃћрЃюрЃбрЃў false-рЃўрЃљ, рЃЊрЃљрЃЉрЃарЃБрЃюрЃЊрЃћрЃЉрЃљ false (рЃЊрЃљ рЃЏрЃћрЃЮрЃарЃћ рЃљрЃа рЃерЃћрЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА)
          - рЃЌрЃБ рЃърЃўрЃарЃЋрЃћрЃџрЃў true-рЃўрЃљ, рЃЊрЃљрЃЉрЃарЃБрЃюрЃЊрЃћрЃЉрЃљ рЃЏрЃћрЃЮрЃарЃћ рЃљрЃарЃњрЃБрЃЏрЃћрЃюрЃбрЃў
          React-рЃерЃў: condition && <Element /> 
          рЃЌрЃБ condition false-рЃўрЃљ, рЃћрЃарЃЌрЃљрЃцрЃћрЃарЃў рЃљрЃа рЃЊрЃљрЃўрЃгрЃћрЃарЃћрЃЉрЃљ
          рЃЌрЃБ true-рЃўрЃљ, Element рЃЊрЃљрЃўрЃгрЃћрЃарЃћрЃЉрЃљ
      */}


      {isLoggedIn && <p> рЃћрЃА рЃбрЃћрЃЦрЃАрЃбрЃў рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃЊрЃљрЃцрЃўрЃЦрЃАрЃўрЃарЃћрЃЉрЃБрЃџрЃў рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃЕрЃљрЃюрЃА</p>}

      {count > 5 && <p>рЃарЃўрЃфрЃ«рЃЋрЃў 5-рЃќрЃћ рЃЏрЃћрЃбрЃўрЃљ!</p>}

   
      {isLoggedIn && userRole === 'admin' && <button>­ЪЌЉ№ИЈ рЃгрЃљрЃерЃљрЃџрЃћ рЃДрЃЋрЃћрЃџрЃљ</button>}


      {/* 
        && operator:
          - рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃарЃЮрЃфрЃљ рЃњрЃўрЃюрЃЊрЃљ рЃарЃљрЃўрЃЏрЃћ рЃљрЃЕрЃЋрЃћрЃюрЃЮ рЃљрЃю рЃљрЃарЃљрЃцрЃћрЃарЃў (if-else рЃўрЃА рЃЏрЃАрЃњрЃљрЃЋрЃАрЃў)
          - isActive && <p>рЃљрЃЦрЃбрЃўрЃБрЃарЃў</p>  Рєњ рЃЌрЃБ true, рЃљрЃЕрЃЋрЃћрЃюрЃћрЃЉрЃА; рЃЌрЃБ false, рЃљрЃа рЃљрЃЕрЃЋрЃћрЃюрЃћрЃЉрЃА
        
        Ternary operator:
          - рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃарЃЮрЃфрЃљ рЃњрЃўрЃюрЃЊрЃљ рЃЮрЃарЃў рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃБрЃџрЃў рЃарЃљрЃЏ рЃљрЃЕрЃЋрЃћрЃюрЃЮ рЃърЃўрЃарЃЮрЃЉрЃўрЃЊрЃљрЃю рЃЊрЃљрЃЏрЃЮрЃЎрЃўрЃЊрЃћрЃЉрЃБрЃџрЃљрЃЊ
          - isActive ? <p>рЃљрЃЦрЃбрЃўрЃБрЃарЃў</p> : <p>рЃљрЃарЃљрЃљрЃЦрЃбрЃўрЃБрЃарЃў</p> Рєњ рЃДрЃЮрЃЋрЃћрЃџрЃЌрЃЋрЃўрЃА рЃарЃљрЃўрЃЏрЃћ рЃљрЃЕрЃЋрЃћрЃюрЃћрЃЉрЃА
      */}


      {userRole === 'admin' && <p>рЃљрЃЊрЃЏрЃўрЃюрЃўрЃА рЃърЃљрЃюрЃћрЃџрЃў рЃљрЃЕрЃЋрЃћрЃюрЃћрЃЉрЃБрЃџрЃў</p>}


      <p>
        {userRole === 'admin' ? 'рЃЌрЃЦрЃЋрЃћрЃю рЃ«рЃљрЃарЃЌ рЃљрЃЊрЃЏрЃўрЃюрЃў' : 'рЃЌрЃЦрЃЋрЃћрЃю рЃљрЃа рЃ«рЃљрЃарЃЌ рЃљрЃЊрЃЏрЃўрЃюрЃў'}
      </p>


      <div style={{ marginTop: '20px', display: 'flex', gap: '10px' }}>
        <button onClick={() => setIsLoggedIn(!isLoggedIn)}>
          {isLoggedIn ? ' рЃњрЃљрЃЏрЃЮрЃАрЃЋрЃџрЃљ' : ' рЃерЃћрЃАрЃЋрЃџрЃљ'}
        </button>

        <button onClick={() => setCount(count + 1)}>
          рЃарЃўрЃфрЃ«рЃЋрЃў рЃњрЃљрЃќрЃарЃЊрЃљ ({count})
        </button>

        <button onClick={() => setUserRole(userRole === 'admin' ? 'user' : 'admin')}>
          рЃарЃЮрЃџрЃў: {userRole}
        </button>
      </div>
    </div>
  );
}
